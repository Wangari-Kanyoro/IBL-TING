x, y, z =input("enter expression ")

if y == "+":
    answer = float(x) + float(z)
    print(answer)

elif y == "-":
    answer = float(x) - float(z)
    print(answer)

elif y == "*":
    answer = float(x) * float(z)
    print(answer)

elif y == "/":
    answer = float(x) / float(z)
    print(answer)

    


